    map: number[][] = JSON.parse(JSON.stringify(this.level1));
    foodTick: number = 0;

    isCollideWall(x: number, y: number) {
        return this.map[x][y] === 1;
    }

    isCollideFood(x: number, y: number) {
        return this.map[x][y] === 2;
    }

    isCollideSuperFood(x: number, y: number) {
        return this.map[x][y] === 3;
    }

    removeFood(x: number, y: number) {
        this.map[x][y] = 0;
    }

    getRandomFoodLocation() {
        let x = Math.floor(Math.random() * 50);
        let y = Math.floor(Math.random() * 50);
        while (this.isCollideWall(x, y)) {
            x = Math.floor(Math.random() * 50);
            y = Math.floor(Math.random() * 50);
        }
        return { x, y };
    }

    reset() {
        this.map = JSON.parse(JSON.stringify(this.level1));
        this.foodTick = 0;
    }

    update() {
        this.foodTick++;
        if (this.foodTick === 20) {
            this.foodTick = 0;
            // too much food on level already
            if (this.map.flat().filter((x) => x === 2).length === 10) {
                return
            }
            const { x, y } = this.getRandomFoodLocation();
            // 3/10 chance to spawn super food
            if (Math.random() < 0.3) {
                this.map[x][y] = 3;
            } else {
                this.map[x][y] = 2;
            }
        }
    }

    render(ctx: CanvasRenderingContext2D) {
        for (let x = 0; x < this.map.length; x++) {
            for (let y = 0; y < this.map[x].length; y++) {
                if (this.map[x][y] === 0) {
                    ctx.fillStyle = 'black' // background
                } else if (this.map[x][y] === 1) {
                    ctx.fillStyle = 'grey' // wall
                } else if (this.map[x][y] === 2) {
                    ctx.fillStyle = 'green' // food
                } else if (this.map[x][y] === 3) {
                    ctx.fillStyle = 'purple' // super food
                }
                ctx.fillRect(x * 10, y * 10, 10, 10)
            }
        }
    }
